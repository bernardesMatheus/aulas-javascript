// Operadores aritméticos
// Adição +
// Subtração -
// Multiplicação *
// Divisão /
// Módulo (ou resto) %

var a = 5;
var b = 10;

console.log(a+b);
console.log(a-b);
console.log(a*b);
console.log(a/b);
console.log(a%b);

// ++ Incremento
// -- Decremento

a++; // a = a + 1;
console.log("A: " + a);

b--; // b = b - 1;
console.log("B: " + b);

a+=2; // a = a + 2;
console.log("A: " + a);

a+=b;
console.log("A: " + a);